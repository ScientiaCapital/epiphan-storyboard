-- =============================================================================
-- ROUTER_JOBS TABLE
-- Stores async router jobs (classify + route + execute)
-- =============================================================================

CREATE TABLE IF NOT EXISTS router_jobs (
    -- Identification
    job_id TEXT PRIMARY KEY,  -- UUID generated by application
    org_id TEXT NOT NULL,  -- Multi-tenant organization identifier

    -- Job configuration
    query TEXT NOT NULL,  -- User query to classify and route
    context JSONB DEFAULT '{}'::jsonb,  -- Optional context for classification
    max_steps INTEGER DEFAULT 10,  -- Maximum execution steps

    -- Status tracking
    status TEXT NOT NULL DEFAULT 'pending' CHECK (status IN (
        'pending',      -- Job created, waiting to start
        'classifying',  -- Running task classification
        'routing',      -- Selecting appropriate chain
        'executing',    -- Executing the chain
        'completed',    -- Successfully completed
        'failed',       -- Failed with error
        'cancelled'     -- Cancelled by user/system
    )),

    -- Classification result (from TaskClassifier)
    classification JSONB,  -- {task_type, confidence, reasoning, extracted_params, recommended_model}

    -- Chain execution result
    chain_result JSONB,  -- Result from chain execution
    error_message TEXT,  -- Error details if status = 'failed'

    -- Timing
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    completed_at TIMESTAMPTZ,  -- When job finished (success or failure)
    execution_time_ms INTEGER  -- Total execution time in milliseconds
);

-- =============================================================================
-- INDEXES
-- Optimized for common query patterns
-- =============================================================================

-- Multi-tenant queries (most common access pattern)
CREATE INDEX IF NOT EXISTS idx_router_jobs_org_id ON router_jobs(org_id);

-- Status monitoring (find pending/failed jobs)
CREATE INDEX IF NOT EXISTS idx_router_jobs_status ON router_jobs(status) WHERE status IN ('pending', 'executing', 'failed');

-- Recent jobs listing (sorted by created_at DESC)
CREATE INDEX IF NOT EXISTS idx_router_jobs_created ON router_jobs(created_at DESC);

-- Task type analytics (on JSONB path)
CREATE INDEX IF NOT EXISTS idx_router_jobs_task_type ON router_jobs((classification->>'task_type')) WHERE classification IS NOT NULL;

-- Composite index for org + status queries (efficient tenant-specific monitoring)
CREATE INDEX IF NOT EXISTS idx_router_jobs_org_status ON router_jobs(org_id, status);

