-- =============================================================================
-- HELPER VIEWS
-- Convenient views for monitoring and analytics
-- =============================================================================

-- Recent jobs: Last 100 jobs with key fields
CREATE OR REPLACE VIEW recent_router_jobs AS
SELECT
    job_id,
    org_id,
    status,
    classification->>'task_type' as task_type,
    (classification->>'confidence')::FLOAT as confidence,
    CASE
        WHEN chain_result->>'success' = 'true' THEN true
        ELSE false
    END as chain_success,
    created_at,
    completed_at,
    execution_time_ms,
    CASE
        WHEN error_message IS NOT NULL THEN SUBSTRING(error_message, 1, 100)
        ELSE NULL
    END as error_preview
FROM router_jobs
ORDER BY created_at DESC
LIMIT 100;

-- Job stats: Aggregated success/failure rates by task_type
CREATE OR REPLACE VIEW router_job_stats AS
SELECT
    classification->>'task_type' as task_type,
    COUNT(*) as total_jobs,
    COUNT(*) FILTER (WHERE status = 'completed') as completed,
    COUNT(*) FILTER (WHERE status = 'failed') as failed,
    COUNT(*) FILTER (WHERE status IN ('pending', 'classifying', 'routing', 'executing')) as in_progress,
    ROUND(
        (COUNT(*) FILTER (WHERE status = 'completed')::NUMERIC / NULLIF(COUNT(*), 0)) * 100,
        2
    ) as success_rate_pct,
    ROUND(AVG((classification->>'confidence')::FLOAT) FILTER (WHERE classification IS NOT NULL), 3) as avg_confidence,
    ROUND(AVG(execution_time_ms) FILTER (WHERE status = 'completed'), 0) as avg_execution_ms,
    PERCENTILE_CONT(0.5) WITHIN GROUP (ORDER BY execution_time_ms) FILTER (WHERE status = 'completed') as median_execution_ms,
    MAX(execution_time_ms) FILTER (WHERE status = 'completed') as max_execution_ms
FROM router_jobs
WHERE created_at > NOW() - INTERVAL '30 days'  -- Last 30 days only
GROUP BY classification->>'task_type'
ORDER BY total_jobs DESC;

-- Organization usage: Jobs per org (for billing/quota monitoring)
CREATE OR REPLACE VIEW router_org_usage AS
SELECT
    org_id,
    COUNT(*) as total_jobs,
    COUNT(*) FILTER (WHERE status = 'completed') as successful_jobs,
    COUNT(*) FILTER (WHERE status = 'failed') as failed_jobs,
    COUNT(*) FILTER (WHERE created_at > NOW() - INTERVAL '24 hours') as jobs_last_24h,
    COUNT(*) FILTER (WHERE created_at > NOW() - INTERVAL '7 days') as jobs_last_7d,
    MAX(created_at) as last_job_at,
    ROUND(AVG(execution_time_ms) FILTER (WHERE status = 'completed'), 0) as avg_execution_ms
FROM router_jobs
GROUP BY org_id
ORDER BY total_jobs DESC;

-- Failed jobs: Recent failures for debugging
CREATE OR REPLACE VIEW router_failures AS
SELECT
    job_id,
    org_id,
    classification->>'task_type' as task_type,
    query,
    created_at,
    completed_at,
    execution_time_ms,
    error_message,
    context,
    classification,
    chain_result
FROM router_jobs
WHERE status = 'failed'
ORDER BY created_at DESC
LIMIT 50;

-- Classification analytics: Confidence distribution by task type
CREATE OR REPLACE VIEW router_classification_analytics AS
SELECT
    classification->>'task_type' as task_type,
    COUNT(*) as total_classifications,
    ROUND(AVG((classification->>'confidence')::FLOAT), 3) as avg_confidence,
    ROUND(MIN((classification->>'confidence')::FLOAT), 3) as min_confidence,
    ROUND(MAX((classification->>'confidence')::FLOAT), 3) as max_confidence,
    COUNT(*) FILTER (WHERE (classification->>'confidence')::FLOAT >= 0.9) as high_confidence_count,
    COUNT(*) FILTER (WHERE (classification->>'confidence')::FLOAT >= 0.7 AND (classification->>'confidence')::FLOAT < 0.9) as medium_confidence_count,
    COUNT(*) FILTER (WHERE (classification->>'confidence')::FLOAT < 0.7) as low_confidence_count
FROM router_jobs
WHERE classification IS NOT NULL
  AND created_at > NOW() - INTERVAL '30 days'
GROUP BY classification->>'task_type'
ORDER BY total_classifications DESC;

