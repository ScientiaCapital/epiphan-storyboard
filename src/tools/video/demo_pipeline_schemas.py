class DemoPipelineRequest(BaseModel):
    """API request for POST /video/demo-pipeline.

    Three entry points:
    1. understanding dict (from a completed storyboard)
    2. storyboard_job_id (fetch understanding from an existing job)
    3. raw content fields (headline + pain_point + business_value)
    """

    # Entry point 1: Full understanding dict
    understanding: dict[str, Any] | None = Field(
        default=None,
        description="StoryboardUnderstanding dict (from completed storyboard job)",
    )

    # Entry point 2: Reference to existing storyboard job
    storyboard_job_id: str | None = Field(
        default=None,
        description="Job ID of completed storyboard to extract understanding from",
    )

    # Entry point 3: Raw content
    headline: str | None = Field(
        default=None, description="Headline for scene generation"
    )
    pain_point: str | None = Field(
        default=None, description="Pain point to address in scenes"
    )
    business_value: str | None = Field(
        default=None, description="Business value proposition"
    )

    # Common parameters
    persona: str = Field(default="av_director", description="Target persona")
    vertical: str = Field(default="higher_ed", description="Target vertical")
    product_focus: str = Field(default="pearl_mini", description="Product to feature")
    skip_video_generation: bool = Field(
        default=False,
        description="If True, only extract scenes (no video generation). Useful for previewing.",
    )
    provider: str = Field(default="kling", description="Video generation provider")

    model_config = {"extra": "forbid"}

    @field_validator("understanding", mode="before")
    @classmethod
    def validate_has_input(cls, v: dict | None, info: Any) -> dict | None:
        """Ensure at least one input source is provided."""
        return v


class DemoPipelineResponse(BaseModel):
    """API response for POST /video/demo-pipeline (202 Accepted)."""

    job_id: str = Field(..., description="Job ID for polling")
    status: DemoPipelineStatus = Field(..., description="Current job status")
    poll_url: str = Field(..., description="URL to poll for status")

    model_config = {"extra": "forbid"}


class DemoPipelineStatusResponse(BaseModel):
    """API response for GET /video/demo-pipeline/jobs/{job_id}."""

    job_id: str = Field(..., description="Unique job identifier")
    status: DemoPipelineStatus = Field(..., description="Current pipeline status")
    scene_extraction: dict[str, Any] | None = Field(
        default=None, description="Scene extraction result (when available)"
    )
    video_assets: dict[str, Any] | None = Field(
        default=None, description="Video assets result (when available)"
    )
    error_message: str | None = Field(
        default=None, description="Error message (when failed)"
    )
    execution_time_ms: int | None = Field(
        default=None, description="Total execution time in ms"
    )
    created_at: datetime = Field(..., description="Job creation timestamp")
    completed_at: datetime | None = Field(
        default=None, description="Job completion timestamp"
    )

    model_config = {"extra": "forbid"}
